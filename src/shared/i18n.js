    // Cached current language. Updated by setLanguage() and the storage
    // change listener wired in shell.js. Default 'en' until the popup has
    // a chance to read chrome.storage.sync.uiLanguage.
    let current = 'en';

    function get() { return current; }

    function set(code) {
        if (!STRINGS[code]) code = 'en';
        current = code;
        try { document.documentElement.setAttribute('lang', code); } catch (_) {}
    }

    function t(key, vars) {
        let s = NON_TRANSLATED[key];
        if (s === undefined) {
            const tbl = STRINGS[current] || STRINGS.en;
            s = tbl[key];
            if (s === undefined) s = STRINGS.en[key];
        }
        if (s === undefined) s = key;
        if (vars && typeof vars === 'object') {
            s = s.replace(/\{(\w+)\}/g, (_m, k) =>
                Object.prototype.hasOwnProperty.call(vars, k) ? String(vars[k]) : `{${k}}`);
        }
        return s;
    }

